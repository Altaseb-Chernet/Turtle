import turtle  

alta = turtle.Turtle()
alta.screen.bgcolor('blue')
alta.screen.title('turtle shapes')
alta.color('gold')
alta.begin_fill()
alta.pencolor('green')
alta.pensize(10)
alta.forward(100)
alta.left(90)
alta.forward(100)
alta.left(90)
alta.forward(100)
alta.left(90)
alta.forward(100)
alta.left(90)

alta.penup()
alta.forward(150)
alta.pendown()

alta.forward(100)
alta.left(90)
alta.forward(100)
alta.left(90)
alta.forward(100)
alta.left(90)
alta.forward(100)
alta.left(90)


alta.end_fill()
turtle.done()